// Helper function to get artwork by ID
export const getArtworkById = (id) => {
  const allArtworks = getAllArtworks();
  return allArtworks.find(art => art.id === id);
};

// Helper function to get related artworks (same category, excluding current)
export const getRelatedArtworks = (currentId, category) => {
  const allArtworks = getAllArtworks();
  return allArtworks.filter(art => 
    art.category === category && art.id !== currentId
  ).slice(0, 3);
};
